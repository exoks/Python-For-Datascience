# ===[ Imports: ]==============================================================
import numpy as np


# ===[ slice_me: ]=============================================================
def slice_me(family: list, start: int, end: int) -> list:
    family_array = np.array(family)

    if type(family) is not list:
        raise Exception(f"Error: Invalid Type: {type(family)}")
    if family_array.ndim != 2:
        raise Exception("Error: Not a 2D array")

    return (family_array[start: end]).tolist()


# ===[ main: ]=================================================================
def main():
    family = [[1.80, 78.4],
              [2.15, 102.7],
              [2.10, 98.5],
              [1.88, 75.2]]

    print(f"my original shape:\n{np.array(family).shape}")

    sliced_family = slice_me(family, 0, 2)
    print(f"my new shape: {np.array(sliced_family).shape}")
    print(sliced_family)

    sliced_family = slice_me(family, 1, -2)
    print(f"my new shape: {np.array(sliced_family).shape}")
    print(sliced_family)


if __name__ == '__main__':
    main()
