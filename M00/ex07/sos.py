# ===[ ft_len: ]===============================================================
def ft_len(iterable) -> int:
    len = 0
    for x in iterable:
        len += 1
    return (len)


# ===[ check_args: ]===========================================================
def check_args(ac, av) -> bool:
    if (ac != 2):
        raise Exception
    print(f"sos: logger: valid_arg: {av[1]}")
    # NOTE: f"" is called string literal prefix
    input = av[1]
    for char in input:
        if (not (char.isspace() or char.isalpha() or char.isalnum())):
            raise Exception
        print(f"char : {char}")
        return (True)


