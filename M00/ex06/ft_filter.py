# ===[ ft_filter: ]============================================================
def ft_filter(function, iterable):
    """
    ft_filter(function, iterbale) -> iterator
    Return an iterator of the new filtred iterator, the type of iterator
    depends on the iterable type (list, tuple, string, etc)
    if function is none the condition get changed to check if element is not
    None
    """
    if (function is None):
        filtred_iterable = [x for x in iterable if x is not None]
    else:
        filtred_iterable = [x for x in iterable if function(x)]
    return (filtred_iterable.__iter__())


