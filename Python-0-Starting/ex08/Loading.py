# ===[ ft_tqdm: ]==============================================================
def ft_tqdm(lst: range) -> None:
    lst_len = len(lst)
    for index in lst:
        percentage = int(((index + 1) / lst_len) * 100)
        progress_bar = f"{'‚ñà' * percentage:100s}"
        counter = f"{index + 1}/{lst_len}"
        print(f"{percentage}%|{progress_bar}| {counter}", end='\r')
        yield index
    print()

    # INFO:====================================================================
    # * Progress bar component are:                                           |
    #   - Percentage 100%                                                     |
    #   - Visual progress bar [‚ñà]                                             |
    #   - Counter 333/333                                                     |
    #   - Elapsed time [00:01                                                 |
    #   - Remaining time <00:00,                                              |
    #   - Speed (iteration per second) 190.99it/s]                            |
